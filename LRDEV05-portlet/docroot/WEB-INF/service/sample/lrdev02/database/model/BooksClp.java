	public int compareTo(Books books) {
		int value = 0;

		if (getBookId() < books.getBookId()) {
			value = -1;
		}
		else if (getBookId() > books.getBookId()) {
			value = 1;
		}
		else {
			value = 0;
		}

		if (value != 0) {
			return value;
		}

		value = DateUtil.compareTo(getCreateDate(), books.getCreateDate());

		value = value * -1;

		if (value != 0) {
			return value;
		}

		return 0;
	}

